package June10_Loops;

//****difference for interview
// syntax difference
// in do while loop, first iteration will get executed irrespective of condition check (ex. gas booking)
// in while loop,condition is checked for every iteration

public class DoWhileLoop {
	
	
	public static void main(String[] args) {
		
		
		
		int i=1;        //initialization
		
		do
		{
			System.out.println("java is easy");         // same statement execution again and again
			i++;								    	// increment / decrement
		}    while(i<=3);         //condition check
		
		
	}

}
